package Domain.Models;


/**
 * Разрешение камеры.
 */
public class Resolution {
    int Width;
    int Height;

    public int getWidth() {
        return Width;
    }

    public void setWidth(int width) {
        Width = width;
    }

    public int getHeight() {
        return Height;
    }

    public void setHeight(int height) {
        Height = height;
    }
}
